import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOProps {
    title?: string;
    description?: string;
    image?: string;
    url?: string;
    type?: string;
}

export const SEO: React.FC<SEOProps> = ({
    title = 'Carvo | חווית נהיגה ושירותי דרך מתקדמים',
    description = 'Carvo מספקת פתרונות חדשניים לבטיחות בדרכים, ערכות חילוץ, ובוט AI לעזרה במצבי חירום. משדרגים את תרבות הנהיגה בישראל.',
    image = 'https://carvo.co.il/og-image.png',
    url = 'https://carvo.co.il',
    type = 'website'
}) => {
    const fullTitle = title.includes('Carvo') ? title : `${title} | Carvo`;

    return (
        <Helmet>
            {/* Standard Metadata */}
            <title>{fullTitle}</title>
            <meta name="description" content={description} />
            <link rel="canonical" href={url} />

            {/* Open Graph / Facebook */}
            <meta property="og:type" content={type} />
            <meta property="og:url" content={url} />
            <meta property="og:title" content={fullTitle} />
            <meta property="og:description" content={description} />
            <meta property="og:image" content={image} />

            {/* Twitter */}
            <meta property="twitter:card" content="summary_large_image" />
            <meta property="twitter:url" content={url} />
            <meta property="twitter:title" content={fullTitle} />
            <meta property="twitter:description" content={description} />
            <meta property="twitter:image" content={image} />
        </Helmet>
    );
};
